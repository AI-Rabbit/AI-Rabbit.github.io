function applyLang(lang) {
    const data = i18nData[lang];
    if (!data) return;
    document.documentElement.setAttribute('data-lang', lang);
    document.documentElement.setAttribute('lang', lang === 'zh' ? 'zh-CN' : 'en');
    document.title = data.pageTitle;

    document.body.classList.add('lang-switching');
    setTimeout(() => {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (data[key] !== undefined) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.value = data[key];
                } else {
                    el.innerHTML = data[key];
                }
            }
        });

        document.querySelectorAll('.hero-name, .hero-name-cn').forEach(el => {
            el.classList.remove('name-zh', 'name-en');
            if (lang === 'zh') {
                if (el.getAttribute('data-i18n') === 'heroName') el.classList.add('name-zh');
                if (el.getAttribute('data-i18n') === 'heroNameCn') el.classList.add('name-en');
            }
        });

        setTimeout(() => {
            document.body.classList.remove('lang-switching');
        }, 50);
    }, 150);

    localStorage.setItem('lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('lang') || 'en';
    applyLang(savedLang);

    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        langToggle.addEventListener('click', () => {
            const currentLang = document.documentElement.getAttribute('data-lang') || 'en';
            const newLang = currentLang === 'zh' ? 'en' : 'zh';
            applyLang(newLang);
        });
    }

    const sections = document.querySelectorAll('.section');
    const observerOptions = {
        root: null,
        rootMargin: '0px 0px -80px 0px',
        threshold: 0.1
    };

    const sectionObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                sectionObserver.unobserve(entry.target);
            }
        });
    }, observerOptions);

    sections.forEach(section => {
        sectionObserver.observe(section);
    });

    const filterBtns = document.querySelectorAll('.filter-btn');
    const pubItems = document.querySelectorAll('.pub-item');

    filterBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            filterBtns.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');

            const filter = btn.dataset.filter;

            pubItems.forEach(item => {
                const types = item.dataset.type.split(' ');
                if (filter === 'all' || types.includes(filter)) {
                    item.classList.remove('hidden');
                    item.style.animation = 'fadeIn 0.4s ease both';
                } else {
                    item.classList.add('hidden');
                }
            });
        });
    });

    const spectrumLine = document.getElementById('spectrumLine');
    if (spectrumLine) {
        let ticking = false;
        window.addEventListener('scroll', () => {
            if (!ticking) {
                requestAnimationFrame(() => {
                    const scrollTop = window.scrollY;
                    const docHeight = document.documentElement.scrollHeight - window.innerHeight;
                    const scrollPercent = docHeight > 0 ? scrollTop / docHeight : 0;
                    const heightPercent = 15 + scrollPercent * 70;
                    spectrumLine.style.background = `linear-gradient(
                        to bottom,
                        transparent 0%,
                        #b8860b ${heightPercent - 15}%,
                        #d4a835 ${heightPercent}%,
                        #b8860b ${heightPercent + 5}%,
                        transparent 100%
                    )`;
                    ticking = false;
                });
                ticking = true;
            }
        });
    }

    pubItems.forEach(item => {
        const num = item.querySelector('.pub-number');
        if (num) {
            item.addEventListener('mouseenter', () => {
                num.style.color = '#b8860b';
            });
            item.addEventListener('mouseleave', () => {
                num.style.color = '';
            });
        }
    });

    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', (e) => {
            e.preventDefault();
            const target = document.querySelector(anchor.getAttribute('href'));
            if (target) {
                target.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }
        });
    });
});